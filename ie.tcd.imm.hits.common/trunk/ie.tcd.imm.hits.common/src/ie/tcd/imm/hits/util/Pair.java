package ie.tcd.imm.hits.util;

import java.io.Serializable;

import javax.annotation.CheckReturnValue;
import javax.annotation.Nonnull;
import javax.annotation.Nullable;
import javax.annotation.concurrent.Immutable;

/**
 * Holds two (different possibly different type) values.
 * <p>
 * {@link Serializable} if and only if both of the type parameters are
 * {@link Serializable}.
 * 
 * @author <a href="mailto:bakosg@tcd.ie">Gabor Bakos</a>
 * @param <Left>
 *            The type of the first/left value.
 * @param <Right>
 *            The type of the second/right value.
 * @deprecated Use {@link org.knime.core.util.Pair} instead.
 */
@Immutable
@Nonnull
@CheckReturnValue
@Deprecated
public class Pair<Left, Right> implements Serializable {
	private static final long serialVersionUID = 2771814364666313214L;

	private @Nullable final Left left;
	private @Nullable final Right right;

	/**
	 * Constructs the {@link Pair}.
	 * 
	 * @param left
	 *            The first/left object.
	 * @param right
	 *            The second, right object.
	 * @deprecated Use the factory methods instead.
	 */
	@Deprecated
	public Pair(@Nullable final Left left, @Nullable final Right right) {
		super();
		this.left = left;
		this.right = right;
	}

	/**
	 * Creates a new {@link Pair}. This is a factory method.
	 * 
	 * @param <Left>
	 *            Type of the first object.
	 * @param <Right>
	 *            Type of the second object.
	 * @param left
	 *            The first object.
	 * @param right
	 *            The second object.
	 * @return A new {@link Pair} of the objects.
	 */
	public static <Left, Right> Pair<Left, Right> apply(final Left left,
			final Right right) {
		return new Pair<Left, Right>(left, right);
	}

	/**
	 * A FindBugs safe version to create a {@link Pair} with empty first node.
	 * 
	 * @param <Left>
	 *            Type of the first object.
	 * @param <Right>
	 *            Type of the second object.
	 * @param right
	 *            The second object.
	 * @return A new {@link Pair} of {@code null} and the object.
	 */
	//@SuppressFBWarnings("NP")
	public static <Left, Right> Pair<Left, Right> leftNull(final Right right) {
		return apply(null, right);
	}

	/**
	 * A FindBugs safe version to create a {@link Pair} with empty second node.
	 * 
	 * @param <Left>
	 *            Type of the first object.
	 * @param <Right>
	 *            Type of the second object.
	 * @param left
	 *            The first object.
	 * @return A new {@link Pair} of the object and {@code null}.
	 */
	//@SuppressFBWarnings("NP")
	public static <Left, Right> Pair<Left, Right> rightNull(final Left left) {
		return apply(left, null);
	}

	/**
	 * A FindBugs safe version to get a {@link Pair} without objects.
	 * 
	 * @param <Left>
	 *            Type of the first object.
	 * @param <Right>
	 *            Type of the second object.
	 * @return A new {@link Pair} of the object and {@code null}.
	 */
	//@SuppressFBWarnings("NP")
	public static <Left, Right> Pair<Left, Right> empty() {
		return apply(null, null);
	}

	/**
	 * Change the order of values.
	 * 
	 * @return A {@link Pair} in a flipped order.
	 */
	public Pair<Right, Left> flip() {
		return Pair.apply(right, left);
	}

	/**
	 * @return The first/left object.
	 */
	public Left getLeft() {
		return left;
	}

	/**
	 * @return The second/right object.
	 */
	public Right getRight() {
		return right;
	}

	/**
	 * @return {#code (}first object's {link Object#toString()}{@code , }second
	 *         object's {link Object#toString()}
	 */
	@Override
	public String toString() {
		return "(" + left + ", " + right + ")";
	}

	/**
	 * {@inheritDoc}
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + (left == null ? 0 : left.hashCode());
		result = prime * result + (right == null ? 0 : right.hashCode());
		return result;
	}

	/**
	 * Checks for equality of this and {@code obj}.
	 * 
	 * @param obj
	 *            Preferably other {@link Pair} object.
	 * @return The value of the equality check generated by eclipse.
	 */
	@Override
	public boolean equals(final Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		final Pair<?, ?> other = (Pair<?, ?>) obj;
		if (left == null) {
			if (other.left != null) {
				return false;
			}
		} else if (!left.equals(other.left)) {
			return false;
		}
		if (right == null) {
			if (other.right != null) {
				return false;
			}
		} else if (!right.equals(other.right)) {
			return false;
		}
		return true;
	}

	/**
	 * Comparable {@link Pair} implementation.
	 * 
	 * @author <a href="mailto:bakosg@tcd.ie">Gabor Bakos</a>
	 * 
	 * @param <Left>
	 *            The type of the first/left value.
	 * @param <Right>
	 *            The type of the second/right value.
	 */
	public static final class ComparablePair<Left extends Comparable<Left>, Right extends Comparable<Right>>
			extends Pair<Left, Right> implements
			Comparable<ComparablePair<Left, Right>> {
		private static final long serialVersionUID = 8726172643276141148L;

		/**
		 * Constructs a ComparablePair.
		 * 
		 * @param left
		 *            The left value.
		 * @param right
		 *            The right value.
		 */
		public ComparablePair(final Left left, final Right right) {
			super(left, right);
		}

		@Override
		public int compareTo(final ComparablePair<Left, Right> o) {
			final int compareTo = getLeft().compareTo(o.getLeft());
			if (compareTo != 0) {
				return compareTo;
			}
			return getRight().compareTo(o.getRight());
		}
	}
}
